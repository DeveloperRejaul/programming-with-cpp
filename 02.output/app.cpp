#include <iostream>
using namespace std;


int main() {
    cout << "Hello World";
    cout << "I am learning C++";
    
    
    // New line 
    cout << "Hello World! \n";
    cout << "I am learning C++ \n";


    // print number 
    cout << 3 << "\n";
    // create blank line
    cout << "\n\n";
    cout << 2 * 5 << "\n";

    // anather way to print new line
    cout << "Hello World!" << endl;
    cout << 2 * 5;

    return 0;
}
